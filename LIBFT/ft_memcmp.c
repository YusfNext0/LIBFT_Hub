#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	
	i = 0;
	unsigned char *str1 = (unsigned char*)s1;
    unsigned char *str2 = (unsigned char*)s2;
	if (n == 0)
		return (0);
	while (str1[i] == str2[i] && i < n - 1)
		i++;
	return (str1[i] - str2[i]);
}