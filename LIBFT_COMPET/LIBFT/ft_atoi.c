#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	num;
	int	signe;

	i = 0;
	num = 0;
	signe = 1;
	while ((str[i] == 32)|| (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	return (num * signe);
}
